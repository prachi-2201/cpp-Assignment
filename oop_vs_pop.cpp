//What is the difference between OOP and POP?
#include<iostream>
using namespace std;
main()
{
	cout<<"\nWhat is the difference between OOP and POP?";
	
	//oop difference
	
	cout<<"\n\nOOP(Object Oriented Programming)";
	
	cout<<"\n\n-->Program is divided into objects.";
	cout<<"\n-->Bottom-up approach.";
	cout<<"\n-->Inheritance property is used.";
	cout<<"\n-->It uses access specifier.";
	cout<<"\n-->Encapsulation is used to hide the data.";
	cout<<"\n-->Concept of virtual function.";
	cout<<"\n-->Object functions are linked through message passing.";
	cout<<"\n-->Adding new data and functions is easy";
	cout<<"\n-->The existing code can be reused.";
	cout<<"\n-->use for solving big problems.";
	cout<<"\n-->C++, Java.\n";
	
	//pop difference
	cout<<"\n\nPOP(Procedural Oriented Programming)";
	
	cout<<"\n\n-->Program is divided into functions.";
	cout<<"\n-->Top-down approach.";
	cout<<"\n-->Inheritance is not allowed.";
	cout<<"\n-->It doesn?t use access specifier.";
	cout<<"\n-->No data hiding.";
	cout<<"\n-->No virtual function.";
	cout<<"\n-->Parts of program are linked through parameter passing.";
	cout<<"\n-->Expanding new data and functions is not easy.";
	cout<<"\n-->No code reusability.";
	cout<<"\n-->Not suitable for solving big problems.";
	cout<<"\n-->C, Pascal.";
}	

